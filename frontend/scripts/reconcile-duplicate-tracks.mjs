/**
 * One-time reconciliation of duplicate tracks: the same song imported under
 * several different MusicBrainz containers for the same artist (Album vs EP
 * vs Single all carrying the exact same recording — MusicBrainz models each
 * as its own release-group, each with its own track-position mbid, so
 * tracks.mbid UNIQUE never catches this). See trackCanonical.mjs.
 *
 * IMPORTANT — this is NOT the same situation as a Live album that happens to
 * perform every song from a studio album: that's two legitimate, distinct
 * releases (a live performance is a different recording the user may want to
 * rate separately), not a stray redundant import. Discovered the hard way on
 * this catalog: several artists have a Live album sharing a dozen+ track
 * titles with their studio album — auto-merging those would gut the Live
 * album down to zero tracks and delete it outright. So before touching
 * anything, this script computes how many titles each pair of albums (same
 * artist) has in common — pairs sharing more than WHOLE_ALBUM_OVERLAP_THRESHOLD
 * titles are treated as "two distinct releases" and only ever reported, never
 * auto-merged. Only pairs sharing a small handful of titles (the actual
 * "Riptide" pattern — one or two stray songs, not a whole parallel tracklist)
 * are processed automatically.
 *
 * For each duplicate group (same artist_id + canonical_title) NOT part of a
 * flagged whole-album-overlap pair:
 *   1. Picks the track to KEEP: prefer the one on a full Album over an
 *      EP/Single/other container (confirmed product decision — searching
 *      "Titres" for a song that exists as both should surface the album
 *      version), then the most engagement (track_diary_entries count), then
 *      earliest created_at.
 *   2. Reattributes track_diary_entries (+ album_id/artist_id), dropping a
 *      row instead of reattributing it when that would violate
 *      UNIQUE(user_id, track_id, listened_at) (the user already rated the
 *      kept version on the same day).
 *   3. Reattributes recommendation_feedback ("pas pour moi" signal), same
 *      drop-on-conflict rule for its UNIQUE(user_id, track_id).
 *   4. Reattributes list_items (no DB-level unique constraint on track_id,
 *      so always reattributed — review duplicates manually after if ever
 *      needed).
 *   5. Deletes external_ids for the removed track, then the track row
 *      itself (cascades user_track_recommendations + track_metadata —
 *      both are caches regenerated by the nightly ML batch / on-demand
 *      streaming-link fetch, safe to lose).
 *   6. If the duplicate's album has zero tracks left afterwards AND no
 *      engagement of its own (diary_entries, saved_albums,
 *      user_favorite_albums, curator_picks, list_items pointing at the
 *      album itself) — deletes the now-empty album too (same as
 *      repair-various-artists-empty.mjs's pattern). Otherwise leaves the
 *      album as-is and just reports it.
 *
 * Usage (from frontend/ directory):
 *   node --env-file=.env.local scripts/reconcile-duplicate-tracks.mjs            (dry-run, default)
 *   node --env-file=.env.local scripts/reconcile-duplicate-tracks.mjs --apply    (writes for real)
 *
 * Full per-group detail goes to scripts/track-duplicates-report.txt (this
 * catalog has 168+ groups — too much for a terminal). Only a summary prints
 * to the console.
 */

import { createClient } from '@supabase/supabase-js';
import { writeFileSync } from 'fs';

const APPLY = process.argv.includes('--apply');
const REPORT_PATH = new URL('./track-duplicates-report.txt', import.meta.url);
const reportLines = [];
function log(line) {
  reportLines.push(line);
}
// A pair of albums (same artist) sharing more titles than this is treated as
// two distinct releases (e.g. Live album vs studio album) and only reported,
// never auto-merged — see the module doc comment above.
const WHOLE_ALBUM_OVERLAP_THRESHOLD = 2;
// Generic filler-track titles are not a reliable "same song" signal — an artist
// can have a completely different, unrelated "Intro"/"Outro"/"Interlude" on
// every album. Title-matching alone can't tell those apart, so these are
// always flagged for manual review instead of auto-merged.
const GENERIC_TITLES = new Set(['intro', 'outro', 'interlude', 'skit', 'prologue', 'reprise', 'untitled', 'bonus track']);

const supabase = createClient(
  process.env.NEXT_PUBLIC_SUPABASE_URL,
  process.env.SUPABASE_SERVICE_KEY
);

function albumPairKey(albumIdA, albumIdB) {
  return [albumIdA, albumIdB].sort().join('|||');
}

async function fetchAllCanonicalTracks() {
  // PostgREST caps unpaginated selects at 1000 rows by default — see the same
  // pitfall documented in reconcile-duplicate-albums.mjs.
  const PAGE_SIZE = 1000;
  const all = [];
  for (let from = 0; ; from += PAGE_SIZE) {
    const { data, error } = await supabase
      .from('tracks')
      .select('id, artist_id, album_id, canonical_title, title, created_at')
      .not('canonical_title', 'is', null)
      .range(from, from + PAGE_SIZE - 1);
    if (error) throw new Error(`fetch tracks failed: ${error.message}`);
    all.push(...data);
    if (data.length < PAGE_SIZE) break;
  }
  return all;
}

function findDuplicateGroups(allTracks) {
  const groups = new Map();
  for (const track of allTracks) {
    const key = `${track.artist_id}|||${track.canonical_title}`;
    if (!groups.has(key)) groups.set(key, []);
    groups.get(key).push(track);
  }
  // Only groups spanning more than one ALBUM are real cross-container duplicates —
  // two tracks with the same title on the SAME album (rare MB data artifact) are
  // out of scope for this script.
  return [...groups.values()].filter((g) => g.length > 1 && new Set(g.map((t) => t.album_id)).size > 1);
}

/** Maps each (albumA, albumB) pair to how many distinct canonical_titles they
 *  have in common, across the whole catalog — used to tell "one stray
 *  redundant song" apart from "two distinct full releases" (see doc comment). */
function computeAlbumPairOverlaps(duplicateGroups) {
  const overlaps = new Map();
  for (const group of duplicateGroups) {
    const albumIds = [...new Set(group.map((t) => t.album_id))];
    for (let i = 0; i < albumIds.length; i++) {
      for (let j = i + 1; j < albumIds.length; j++) {
        const key = albumPairKey(albumIds[i], albumIds[j]);
        overlaps.set(key, (overlaps.get(key) ?? 0) + 1);
      }
    }
  }
  return overlaps;
}

async function getAlbumTypes(albumIds) {
  const { data, error } = await supabase.from('albums').select('id, type').in('id', albumIds);
  if (error) throw new Error(`fetch albums failed: ${error.message}`);
  return new Map(data.map((a) => [a.id, a.type]));
}

async function getEngagementCounts(trackIds) {
  const { data, error } = await supabase
    .from('track_diary_entries')
    .select('track_id')
    .in('track_id', trackIds);
  if (error) throw new Error(`fetch track_diary_entries failed: ${error.message}`);
  const counts = new Map(trackIds.map((id) => [id, 0]));
  for (const row of data) counts.set(row.track_id, (counts.get(row.track_id) ?? 0) + 1);
  return counts;
}

function typeRank(type) {
  return type === 'Album' ? 3 : type === 'EP' ? 2 : type === 'Single' ? 1 : 0;
}

function pickKept(group, albumTypes, engagementCounts) {
  return [...group].sort((a, b) => {
    const rankA = typeRank(albumTypes.get(a.album_id));
    const rankB = typeRank(albumTypes.get(b.album_id));
    if (rankA !== rankB) return rankB - rankA;
    const engA = engagementCounts.get(a.id) ?? 0;
    const engB = engagementCounts.get(b.id) ?? 0;
    if (engA !== engB) return engB - engA;
    return new Date(a.created_at) - new Date(b.created_at);
  })[0];
}

/** Reattribute rows from dupId to keepId for a user-scoped table with a
 *  UNIQUE(user_id, ...) constraint — drop the dup row instead of reattributing
 *  it when the user already has a row for the kept track. */
async function reattributeUserScoped(table, dupTrackId, keepTrackId, extraUpdate = {}) {
  const { data: dupRows, error } = await supabase.from(table).select('id, user_id').eq('track_id', dupTrackId);
  if (error) throw new Error(`fetch ${table} failed: ${error.message}`);
  let moved = 0;
  let dropped = 0;
  for (const row of dupRows) {
    if (APPLY) {
      const { data: existing } = await supabase
        .from(table)
        .select('id')
        .eq('user_id', row.user_id)
        .eq('track_id', keepTrackId)
        .maybeSingle();
      if (existing) {
        await supabase.from(table).delete().eq('id', row.id);
        dropped++;
      } else {
        await supabase.from(table).update({ track_id: keepTrackId, ...extraUpdate }).eq('id', row.id);
        moved++;
      }
    } else {
      moved++; // dry-run: can't reliably predict conflicts without locking, just report volume
    }
  }
  return { moved, dropped };
}

async function albumIsEmptyAndUnengaged(albumId) {
  const [{ count: trackCount }, { count: diaryCount }, { count: savedCount }, { count: favCount }, { count: curatorCount }, { count: listCount }] =
    await Promise.all([
      supabase.from('tracks').select('id', { count: 'exact', head: true }).eq('album_id', albumId),
      supabase.from('diary_entries').select('id', { count: 'exact', head: true }).eq('album_id', albumId),
      supabase.from('saved_albums').select('id', { count: 'exact', head: true }).eq('album_id', albumId),
      supabase.from('user_favorite_albums').select('id', { count: 'exact', head: true }).eq('album_id', albumId),
      supabase.from('curator_picks').select('id', { count: 'exact', head: true }).eq('album_id', albumId),
      supabase.from('list_items').select('id', { count: 'exact', head: true }).eq('album_id', albumId),
    ]);
  return (trackCount ?? 0) === 0 && [diaryCount, savedCount, favCount, curatorCount, listCount].every((c) => (c ?? 0) === 0);
}

async function reconcileGroup(group, pairOverlaps, stats) {
  const albumIds = [...new Set(group.map((t) => t.album_id))];
  const [albumTypes, engagementCounts] = await Promise.all([
    getAlbumTypes(albumIds),
    getEngagementCounts(group.map((t) => t.id)),
  ]);
  const kept = pickKept(group, albumTypes, engagementCounts);
  const duplicates = group.filter((t) => t.id !== kept.id);

  log(`\n=== "${kept.title}" (artist ${kept.artist_id}) — keeping track ${kept.id} on album ${kept.album_id} (${albumTypes.get(kept.album_id)}) ===`);

  for (const dup of duplicates) {
    const overlap = pairOverlaps.get(albumPairKey(kept.album_id, dup.album_id)) ?? 0;
    if (overlap > WHOLE_ALBUM_OVERLAP_THRESHOLD) {
      log(`  ⚠ SKIPPED — album ${dup.album_id} (${albumTypes.get(dup.album_id)}) shares ${overlap} titles with ${kept.album_id} (${albumTypes.get(kept.album_id)}) — looks like two distinct releases (e.g. Live vs studio), not a stray duplicate. Needs manual review, not auto-merging.`);
      stats.skipped++;
      continue;
    }
    if (GENERIC_TITLES.has(kept.canonical_title)) {
      log(`  ⚠ SKIPPED — "${kept.title}" is a generic filler-track title (Intro/Outro/Interlude/...) — title match alone isn't reliable here, needs manual review.`);
      stats.skipped++;
      continue;
    }

    log(`  − duplicate track ${dup.id} on album ${dup.album_id} (${albumTypes.get(dup.album_id)}, ${engagementCounts.get(dup.id) ?? 0} critique(s))`);

    if (APPLY) {
      // 1. track_diary_entries — remap track_id + album_id, drop on conflict
      const { data: entries } = await supabase
        .from('track_diary_entries')
        .select('id, user_id, listened_at')
        .eq('track_id', dup.id);
      for (const entry of entries ?? []) {
        const { data: conflict } = await supabase
          .from('track_diary_entries')
          .select('id')
          .eq('user_id', entry.user_id)
          .eq('track_id', kept.id)
          .eq('listened_at', entry.listened_at)
          .maybeSingle();
        if (conflict) {
          await supabase.from('track_diary_entries').delete().eq('id', entry.id);
        } else {
          await supabase.from('track_diary_entries').update({ track_id: kept.id, album_id: kept.album_id, artist_id: kept.artist_id }).eq('id', entry.id);
        }
      }
      log(`    track_diary_entries: ${entries?.length ?? 0} processed`);

      // 2. recommendation_feedback — UNIQUE(user_id, track_id)
      const fbResult = await reattributeUserScoped('recommendation_feedback', dup.id, kept.id);
      log(`    recommendation_feedback: moved ${fbResult.moved}, dropped ${fbResult.dropped}`);

      // 3. list_items — no unique constraint on track_id, always reattribute
      await supabase.from('list_items').update({ track_id: kept.id }).eq('track_id', dup.id);

      // 4. external_ids for the track itself, then the track row (cascades
      // user_track_recommendations + track_metadata — both regenerable caches)
      await supabase.from('external_ids').delete().eq('entity_type', 'track').eq('entity_id', dup.id);
      const { error: deleteError } = await supabase.from('tracks').delete().eq('id', dup.id);
      if (deleteError) {
        log(`    ✗ delete failed: ${deleteError.message}`);
        stats.errors++;
        continue;
      }
      log(`    ✓ merged into ${kept.id}`);
      stats.merged++;

      // 5. Clean up the now-possibly-empty album
      if (await albumIsEmptyAndUnengaged(dup.album_id)) {
        await supabase.from('external_ids').delete().eq('entity_type', 'album').eq('entity_id', dup.album_id);
        const { error: albumDeleteError } = await supabase.from('albums').delete().eq('id', dup.album_id);
        if (albumDeleteError) log(`    ✗ empty album cleanup failed: ${albumDeleteError.message}`);
        else {
          log(`    ✓ now-empty album ${dup.album_id} deleted`);
          stats.albumsDeleted++;
        }
      } else {
        log(`    ↷ album ${dup.album_id} kept (still has tracks or engagement of its own)`);
      }
    } else {
      log('    [dry-run] would reattribute track_diary_entries/recommendation_feedback/list_items, delete the track, then delete its album if left empty and unengaged');
      stats.merged++;
    }
  }
}

async function main() {
  const allTracks = await fetchAllCanonicalTracks();
  const groups = findDuplicateGroups(allTracks);
  const pairOverlaps = computeAlbumPairOverlaps(groups);

  const wholeAlbumPairs = [...pairOverlaps.entries()].filter(([, count]) => count > WHOLE_ALBUM_OVERLAP_THRESHOLD);
  log(`${APPLY ? 'APPLY' : 'DRY-RUN'} — ${groups.length} duplicate track groups found, ${wholeAlbumPairs.length} album pair(s) flagged as distinct releases (skipped)`);

  const stats = { merged: 0, skipped: 0, errors: 0, albumsDeleted: 0 };
  for (const group of groups) {
    await reconcileGroup(group, pairOverlaps, stats);
  }

  log(`\nDone.${APPLY ? '' : ' Re-run with --apply to write changes.'}`);
  writeFileSync(REPORT_PATH, reportLines.join('\n'), 'utf-8');

  console.log(`${APPLY ? 'APPLY' : 'DRY-RUN'} summary — ${groups.length} groups, ${stats.merged} track(s) ${APPLY ? 'merged' : 'would be merged'}, ${stats.skipped} skipped (distinct releases, manual review), ${stats.errors} error(s), ${stats.albumsDeleted} empty album(s) deleted.`);
  console.log(`Full detail written to ${REPORT_PATH.pathname.replace(/^\/([A-Za-z]):/, '$1:')}`);
}

main().catch((err) => {
  console.error(err);
  process.exit(1);
});
